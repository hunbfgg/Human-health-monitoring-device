void Show_Chinese(u8 x,u16 y,u8 *font)   //在x,y地址显示16*16点阵汉字
{ 
 u8 temp,t,t1,c=1;
 u16 y0=y;
 if(c==1)
 {
  for(t=0;t<32;t++)
  {                            
   temp=*font++;
   for(t1=0;t1<8;t1++)
   {
    if(temp&0x80)
     LCD_Fast_DrawPoint(x,y,POINT_COLOR);
    else if(1)
     LCD_Fast_DrawPoint(x,y,BACK_COLOR); 
    temp<<=1;
    y++;
    if((y-y0)==16)
    {
     y=y0;
     x++;
     break;
    }
   }    
  }
 c=2;
	}
}


